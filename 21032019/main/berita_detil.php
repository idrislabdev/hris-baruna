            	<p>SERANG-Forum Solidaritas Buruh Serang meminta  agar Peraturan Daerah (Perda Ketenagakerjaan yang telah disahkan  beberapa waktu lalu dikaji ulang, karena pasal yang terdapat dalam Perda  tersebut belum menyentuh persoalan-persoalan inti yang dihadapi kaum  buruh.</p>

                <p>Kepada Radar Banten, Minggu (12/4), Koordinator Bidang  Politik Hukum dan HAM Forum Solidaritas Buruh Serang, Argo Priyo  Sujatmiko, mengatakan, pihaknya telah meminta kepada Pansus  Ketenagakerjaan agar tidak terburu-buruh mengesahkan peraturan ini.  Karena masih ada beberapa hal yang belum menyentuh persoalan. &quot;Ada 4  poin yang sangat penting bagi buruh yaitu tenaga kontrak, percaloan  tenaga kerja, kebebasan berserikat, dan upah yang di bawah UMK. Nah,  empat masalah ini belum dijelaskan secara rinci di Perda sehingga kami  khawatir jika dipaksakan untuk dijadikan aturan maka nasib buruh tidak  akan berubah,&quot; kata Priyo.</p>
                
                <p>Mengenai percaloan tenaga kerja,  menurutnya, masih dilakukan oleh oknum. &quot;Para calo ini meminta uang  kepada calon tenaga kerja antara Rp. 500 ribu â€“ Rp. 2 juta padahal calon  tenaga kerja ini hanya jadi karyawan kontrak,&quot; kata Priyo.</p>
                
                <p>Terkait  kritik ini, Kepala Dinas Tenaga Kerja dan Transmigrasi Kabupaten  Serang, Sudrajat, mengungkapkan bahwa Perda yang telah disahkan masih  dikaji di bagian hukum sehingga belum diberlakukan. (kar)</p>
